/*
            O OPERADOR TYPEOF

    Você pode usar o operador (TYPEOF) em JAVASCRIPT para localizar
        o tipo de uma variável no JAVASCRIPT;

    O OPERADOR TYPEOF retornará o tipo de uma variável ou expressão;
*/

let palavra1 = '';      // return (STRING);
let palavra2 = 'Dev';   // return (STRING);
let palavra3 = "";      // return (STRING);
let palavra4 = "Dev";   // return (STRING);

let numero1 = 0;        // return (NUMBER);
let numero2 = 321;      // return (NUMBER);
let numero3 = 321.123;  // return (NUMBER);
let numero4 = (5);      // return (NUMBER);
let numero5 = (5 + 5);  // return (NUMBER);

