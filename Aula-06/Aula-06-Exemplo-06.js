/*
            INDEFINIDO EM JAVASCRIPT

    Em JAVASCRIPT, uma variável sem valor tem o valor de (UNDEFINED);

    O seu TIPO também é (UNDEFINED);
*/

let carro; // valor é (UNDEFINED), tipo também (UNDEFINED);

/*
    Qualquer variável pode ser esvaziada, definindo o seu valor como
        (UNDEFINED). Logo seu TIPO também será (UNDEFINED);
*/

carros = undefined; // valor e tipo são (UNDEFINED);

/*
            VALORES VAZIOS

    Um valor vazio não tem nada a ver com (UNDEFINED);

    Uma (STRING) vazia tem um valor valido por exemplo e um TIPO;
*/

let car = ""; // o valor é ""; o TYPEOF é (STRING);