/*
            FUNÇÕES EM JAVASCRIPT

    Uma FUNÇÃO JAVASCRIPT é um bloco de código projetado para
        executar uma tarefa específica;

    Uma FUNÇÃO JAVASCRIPT é executada quando "algo" a invoca
        (chama);
*/

function soma(params1, params2) {
    return params1 + params2;
}

console.log(soma(5, 5)); // return 10;

/*
            SINTAXE DA FUNÇÃO JAVASCRIPT

    Uma FUNÇÃO JAVASCRIPT é definida pela Palavra-Chave (FUNCTION),
        seguida de um (NOME), seguido por parêntesses ();
    
    Os (NOMES) das (FUNCTIONS) podem conter letras, digitos, sublinhados
        e até mesmo cifrões (mesmas regras das variáveis);

    Os parênteses podem incluir nomes de parâmetros separados por vírgula:
        (PARÂMETROS1, PARÂMETROS2);

    O código a ser executado, pela (FUNÇÃO) é colocado entre chaves {};
*/

function name(params1, params2, params3) {
    // code a ser executado;
}

/*
    Os (PARÂMETROS) da (FUNÇÃO) são listados entre os parênteses ()
        na definição da função;

    Os (PARÂMETROS) = (ARGUMENTOS) da (FUNÇÃO), são como (INSTRUÇÕES) = (VALORES)
        recebidos pela (FUNÇÃO) quando invocada;

    Dentro da (FUNÇÃO), os (ARGUMENTOS) se comportam como variáveis
        Locais;

    // Uma Função é muito parecida com um Procedimento ou uma Sub-rotina, 
        em outras linguagens de programação.
*/